package com.paipeng.pushserver.push.gcm;

public class Notification {
	private String title;
	private String text;
	
	public Notification() {
		
		
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
	
	
}
